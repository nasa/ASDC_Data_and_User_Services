C< 	/DATA/ - REC1
C<	/CONSTS/ - NREP,LWORD
C
C< OUTPUT PARAMETERS:
C<	NBYTES: NUMBER OF BYTES THAT NEEDS TO BE READ FOR REC TWO
C<	ISIZE: LENGTH OF RECORD TWO
C
