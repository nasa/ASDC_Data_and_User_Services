	ZREP = REP - FLOAT(NUM16)
	IF(ZREP .NE. 0) THEN
	  NUM16 = NUM16 + 1
	ENDIF
C
C<	***CALCULATE THE NUMBER OF BYTES AND LENGTH OF REC2
C
	ISIZE = IWORDS(NUM16)*3.75
        NBYTES = 2*ISIZE
C
	RETURN
	END
